int		ft_atoi(char *str)
{
	int		i;
	int		mult;
	int		tot;

	mult = 1;
	tot = 0;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
	{
		mult = -1;
		i++;
	}
	else if (str[i] == '+')
	{
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		tot = tot * 10 + str[i] - 48;
		i++;
	}
	return (mult * tot);
}
